<script>
    import { cart } from '../stores/cart';
    import { user } from '../stores/auth';
    import { goto } from '$app/navigation';
    import { fly } from 'svelte/transition';
    import Icon from '@iconify/svelte';
    import { derived } from 'svelte/store';
  
    let { isOpen = $bindable(false) } = $props();
  
    let authUser = $derived($user);
    let cartItems = $derived($cart);
    let cartCount = $derived($cart.reduce((total, item) => total + item.quantity, 0));
  
    // Calculate totals
    let itemsTotal = $derived(
      $cart.reduce((total, item) => {
        const price = item.discountedPrice || item.price;
        return total + (price * item.quantity);
      }, 0)
    );
  
    let deliveryCharge = $derived(itemsTotal > 50000 ? 0 : 2500); // Free delivery above ₹500
    let handlingCharge = $derived(200); // ₹2 handling charge
    let grandTotal = $derived(itemsTotal + deliveryCharge + handlingCharge);
  
    // Dynamic info box text based on cart contents
    const infoBoxText = derived(cart, $cart => {
      console.log('Cart contents:', $cart);
      const hasService = $cart.some(item => item.is_service);
      const hasProduct = $cart.some(item => !item.is_service);
      if (hasService && hasProduct) {
        return 'You have both products and services in your cart!';
      } else if (hasService) {
        return 'Fast and reliable service booking';
      } else if (hasProduct) {
        return 'Quick and secure product delivery';
      } else {
        return 'Your cart is empty';
      }
    });
  
    function formatPrice(price) {
      return new Intl.NumberFormat('en-IN', { 
        style: 'currency', 
        currency: 'INR',
        minimumFractionDigits: 0,
        maximumFractionDigits: 0
      }).format(price);
    }
  
    function updateQuantity(productId, change) {
      cart.update(items => {
        return items.map(item => {
          if (item.id === productId) {
            const newQuantity = Math.max(0, item.quantity + change);
            return newQuantity === 0 ? null : { ...item, quantity: newQuantity };
          }
          return item;
        }).filter(Boolean);
      });
    }
  
    function removeItem(productId) {
      cart.update(items => items.filter(item => item.id !== productId));
    }
  
    function handleProceedToCheckout() {
      if (!authUser) {
        goto('/login?next=/checkout');
      } else {
        goto('/checkout');
      }
      isOpen = false;
    }
  
    function handleClose() {
      isOpen = false;
    }
  
    // Check for out of stock items
    let outOfStockCount = $derived(
      $cart.filter(item => item.stock <= 0).length
    );
  </script>
  
  <!-- Backdrop -->
  {#if isOpen}
    <div 
      class="fixed inset-0 bg-black bg-opacity-50 z-40"
      onclick={handleClose}
      onkeydown={() => {}}
      role="button"
      tabindex="0"
      transition:fly={{ x: 300, duration: 300 }}
    ></div>
  {/if}
  
  <!-- Cart Sidebar -->
  {#if isOpen}
    <div 
      class="fixed right-0 top-0 h-full w-full max-w-md bg-white shadow-xl z-50 flex flex-col"
      transition:fly={{ x: 300, duration: 300 }}
    >
      <!-- Header -->
      <div class="flex items-center justify-between p-4 border-b border-gray-200">
        <h2 class="text-lg font-semibold text-gray-900">My Cart</h2>
        <button 
          onclick={handleClose}
          class="p-2 hover:bg-gray-100 rounded-full transition-colors"
        >
          <Icon icon="mdi:close" class="w-5 h-5 text-gray-600" />
        </button>
      </div>
  
      <!-- Out of Stock Alert -->
      {#if outOfStockCount > 0}
        <div class="mx-4 mt-4 p-3 bg-red-50 border border-red-200 rounded-lg">
          <div class="flex items-center justify-between">
            <div>
              <p class="text-sm font-medium text-red-800">
                {outOfStockCount} out of stock item{outOfStockCount > 1 ? 's' : ''}...
              </p>
              <p class="text-xs text-red-600">you can continue to checkout</p>
            </div>
            <button class="bg-green-600 hover:bg-green-700 text-white px-3 py-1 rounded text-xs font-medium">
              Review
            </button>
          </div>
        </div>
      {/if}
  
      <!-- Delivery Info -->
      <div class="mx-4 mt-4 p-3 bg-blue-50 border border-blue-200 rounded-lg">
        <div class="flex items-center gap-2">
          <div class="w-6 h-6 bg-blue-600 rounded-full flex items-center justify-center">
            <Icon icon="mdi:cart-outline" class="w-4 h-4 text-white" />
          </div>
          <div>
            <p class="text-sm font-medium text-blue-900">{$infoBoxText}</p>
            <p class="text-xs text-blue-700">Your selected items are ready for checkout</p>
          </div>
        </div>
      </div>
  
      <!-- Cart Items -->
      <div class="flex-1 overflow-y-auto p-4 space-y-4">
        {#if cartItems.length === 0}
          <div class="text-center py-12">
            <Icon icon="mdi:cart-outline" class="w-16 h-16 text-gray-300 mx-auto mb-4" />
            <h3 class="text-lg font-medium text-gray-900 mb-2">Your cart is empty</h3>
            <p class="text-gray-500 mb-4">Add some items to get started</p>
            <button 
              onclick={handleClose}
              class="bg-blue-600 hover:bg-blue-700 text-white px-6 py-2 rounded-lg font-medium"
            >
              Continue Shopping
            </button>
          </div>
        {:else}
          {#each cartItems as item (item.id)}
            <div class="flex items-center gap-3 p-3 bg-gray-50 rounded-lg">
              <!-- Product Image -->
              <div class="w-12 h-12 bg-white rounded border overflow-hidden flex-shrink-0">
                <img 
                  src={item.main_image || item.thumbnail || "/placeholder.svg?height=48&width=48"} 
                  alt={item.name}
                  class="w-full h-full object-cover"
                />
              </div>
  
              <!-- Product Details -->
              <div class="flex-1 min-w-0">
                <h4 class="text-sm font-medium text-gray-900 truncate">{item.name}</h4>
                {#if item.category}
                  <p class="text-xs text-gray-500">{item.category.name}</p>
                {/if}
                
                <!-- Price -->
                <div class="flex items-center gap-2 mt-1">
                  <span class="text-sm font-semibold text-gray-900">
                    {formatPrice(item.discountedPrice || item.price)}
                  </span>
                  {#if item.discountedPrice && item.price > item.discountedPrice}
                    <span class="text-xs text-gray-500 line-through">
                      {formatPrice(item.price)}
                    </span>
                  {/if}
                </div>
  
                <!-- Stock Status -->
                {#if item.stock <= 0}
                  <span class="inline-block text-xs text-red-600 bg-red-100 px-2 py-1 rounded mt-1">
                    Out of Stock
                  </span>
                {/if}
              </div>
  
              <!-- Quantity Controls -->
              <div class="flex items-center gap-2">
                {#if item.quantity === 1}
                  <button 
                    onclick={() => removeItem(item.id)}
                    class="w-8 h-8 flex items-center justify-center border border-red-300 rounded hover:bg-red-50 text-red-600"
                  >
                    <Icon icon="mdi:delete-outline" class="w-4 h-4" />
                  </button>
                {:else}
                  <button 
                    onclick={() => updateQuantity(item.id, -1)}
                    class="w-8 h-8 flex items-center justify-center border border-gray-300 rounded hover:bg-gray-50"
                  >
                    <Icon icon="mdi:minus" class="w-4 h-4" />
                  </button>
                {/if}
                
                <span class="w-8 text-center text-sm font-medium">{item.quantity}</span>
                
                <button 
                  onclick={() => updateQuantity(item.id, 1)}
                  class="w-8 h-8 flex items-center justify-center border border-green-300 rounded hover:bg-green-50 text-green-600"
                  disabled={item.quantity >= (item.buy_limit || 10)}
                >
                  <Icon icon="mdi:plus" class="w-4 h-4" />
                </button>
              </div>
            </div>
          {/each}
        {/if}
      </div>
  
      <!-- Bill Details & Checkout -->
      {#if cartItems.length > 0}
        <div class="border-t border-gray-200 p-4 space-y-4">
          <!-- Bill Details -->
          <div class="space-y-2">
            <h3 class="font-semibold text-gray-900 flex items-center gap-2">
              <Icon icon="mdi:receipt-text-outline" class="w-4 h-4" />
              Bill details
            </h3>
            
            <div class="space-y-1 text-sm">
              <div class="flex justify-between">
                <span class="text-gray-600">Items total</span>
                <span class="font-medium">{formatPrice(itemsTotal)}</span>
              </div>
            </div>
            
            <div class="border-t pt-2">
              <div class="flex justify-between items-center">
                <span class="font-semibold text-gray-900">Grand total</span>
                <span class="font-bold text-lg">{formatPrice(grandTotal)}</span>
              </div>
            </div>
          </div>
  
          <!-- Checkout Button -->
          <button 
            onclick={handleProceedToCheckout}
            class="w-full bg-green-600 hover:bg-green-700 text-white font-semibold py-3 px-4 rounded-lg transition-colors flex items-center justify-between"
          >
            <span class="flex items-center gap-2">
              <span class="text-lg font-bold">{formatPrice(grandTotal)}</span>
              <span class="text-sm opacity-90">TOTAL</span>
            </span>
            <span class="flex items-center gap-2">
              {#if !authUser}
                Login to Proceed
                <Icon icon="mdi:login" class="w-4 h-4" />
              {:else}
                Proceed to Checkout
                <Icon icon="mdi:arrow-right" class="w-4 h-4" />
              {/if}
            </span>
          </button>
  
          <!-- Friendly Message -->
          <p class="text-xs text-center text-gray-500">
            Thank you for shopping with us!
          </p>
        </div>
      {/if}
    </div>
  {/if}
  